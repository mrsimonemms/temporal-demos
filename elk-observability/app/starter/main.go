package main

import (
	"context"
	"log"

	elk_observability "github.com/mrsimonemms/temporal-demos/elk-observability"
	"github.com/mrsimonemms/temporal-demos/elk-observability/lib/temporal"
	"go.temporal.io/sdk/client"
)

func main() {
	// The client is a heavyweight object that should be created once per process.
	c, err := temporal.NewClient()
	if err != nil {
		log.Fatalln("Unable to create client", err)
	}
	defer c.Close()

	workflowOptions := client.StartWorkflowOptions{
		ID:        "interceptor_workflowID",
		TaskQueue: "elk-observability",
	}

	we, err := c.ExecuteWorkflow(context.Background(), workflowOptions, elk_observability.Workflow, "Temporal")
	if err != nil {
		log.Fatalln("Unable to execute workflow", err)
	}

	log.Println("Started workflow", "WorkflowID", we.GetID(), "RunID", we.GetRunID())

	// Synchronously wait for the workflow completion.
	var result string
	err = we.Get(context.Background(), &result)
	if err != nil {
		log.Fatalln("Unable get workflow result", err)
	}
	log.Println("Workflow result:", result)
}
